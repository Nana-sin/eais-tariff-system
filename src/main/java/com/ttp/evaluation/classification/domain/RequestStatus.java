package com.ttp.evaluation.classification.domain;

public enum RequestStatus {
    /**
     * Ожидает обработки
     * Начальный статус после создания запроса
     */
    PENDING,

    /**
     * Автоматически классифицировано ML-моделью
     *
     * TODO: Будет использоваться в версии 2.0 при внедрении ML
     * Переход: PENDING → AI_CLASSIFIED (если confidence > 90%)
     * Следующий шаг: EXPERT_REVIEW (эксперт проверяет результат)
     *
     * Зарезервировано для будущего функционала
     */
    AI_CLASSIFIED,

    /**
     * Взято экспертом на проверку
     *
     * TODO: Будет использоваться в версии 2.0
     * Показывает что эксперт начал работу над запросом
     * Предотвращает дублирование работы между экспертами
     *
     * Зарезервировано для будущего функционала
     */
    EXPERT_REVIEW,

    /**
     * Одобрено экспертом
     * Финальный статус - классификация подтверждена
     */
    APPROVED,

    /**
     * Отклонено экспертом
     * Финальный статус - классификация отклонена
     */
    REJECTED
}
